# CalcularITA

def multiplicacao(a, b):
    return a * b
def divisao(a, b):
    return a / b
def adicao(a, b):
    return a + b
def subtracao(a, b):
    return a - b

#Teste das funções
if __name__ == "__main__":
    num1 = 10
    num2 = 5
    print("Multiplicação:", multiplicacao(num1, num2))
    print("Divisão:", divisao(num1, num2))
    print("Adição:", adicao(num1, num2))
    print("Subtração:", subtracao(num1, num2))